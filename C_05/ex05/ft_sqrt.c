int	ft_sqrt(int nb)
{
	unsigned int i;

	i = 1;
	if (nb < 1)
		return (0);
	while (i * i < (unsigned int)nb)
		i++;
	if (i * i == (unsigned int)nb)
		return (i);
	return (0);
}
