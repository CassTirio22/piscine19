int	ft_is_prime(int nb)
{
	unsigned int i;
	unsigned int nbr;

	i = 2;
	if (nb < 2)
		return (0);
	nbr = nb;
	while (i * i <= nbr)
	{
		if (!(nbr % i))
			return (0);
		i++;
	}
	return (1);
}
