#include "ft.h"

void	ft_putnbr(int nbr)
{
	unsigned int	n;

	if (nbr < 0)
	{
		nbr = -nbr;
		ft_putchar('-');
	}
	n = nbr;
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
}
